<template>
	<div :class="{ part: true, readonly: !editing }">
		<h3>
			<span :class="type.iconClass + ' icon-service'" />
			<span>
				{{ type.name }}
			</span>
			<Actions
				:force-menu="true"
				placement="bottom">
				<ActionLink
					icon="icon-info"
					title="More information"
					target="_blank"
					:href="type.infoTarget"
					:close-after-click="true" />
				<ActionButton
					icon="icon-rename"
					:close-after-click="true"
					@click="onEditClick">
					{{ editing ? t('spreed', 'End edition'): t('spreed', 'Edit') }}
				</ActionButton>
				<ActionButton
					icon="icon-delete"
					:close-after-click="true"
					@click="$emit('delete-part')">
					{{ t('spreed', 'Delete') }}
				</ActionButton>
			</Actions>
		</h3>
		<div v-for="(field, key) in type.fields" :key="key">
			<div v-if="field.type === 'checkbox'" class="checkbox-container">
				<input
					:id="key + '-' + num"
					v-model="part[key]"
					:type="field.type"
					:class="classesOf(key)"
					:disabled="!editing">
				<label :for="key + '-' + num">
					{{ field.labelText }}
				</label>
			</div>
			<div v-else>
				<label :for="key + '-' + num" class="hidden-visually">
					{{ field.placeholder }}
				</label>
				<input
					:id="key + '-' + num"
					v-model="part[key]"
					:type="field.type"
					:class="classesOf(key)"
					:placeholder="field.placeholder"
					:readonly="readonly || !editing"
					@focus="readonly = false">
			</div>
		</div>
	</div>
</template>

<script>
import Actions from '@nextcloud/vue/dist/Components/Actions'
import ActionButton from '@nextcloud/vue/dist/Components/ActionButton'
import ActionLink from '@nextcloud/vue/dist/Components/ActionLink'

export default {
	name: 'BridgePart',
	components: {
		Actions,
		ActionButton,
		ActionLink,
	},

	mixins: [
	],

	props: {
		num: {
			type: Number,
			required: true,
		},
		part: {
			type: Object,
			required: true,
		},
		type: {
			type: Object,
			required: true,
		},
		deletable: {
			type: Boolean,
			default: true,
		},
		editing: {
			type: Boolean,
			default: false,
		},
	},

	data() {
		return {
			readonly: true,
		}
	},

	computed: {
	},

	methods: {
		classesOf(name) {
			const classes = {
				icon: true,
			}
			classes[this.type.fields[name].icon] = true
			return classes
		},
		onEditClick() {
			this.$emit('edit-clicked')
		},
	},
}
</script>

